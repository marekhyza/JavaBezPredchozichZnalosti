package KapitolaTri;

public class KombinovanyOperator {
  public static void main(String[] args) {
         int a = 5;
        System.out.println("a = 5: " + a);
        a += 5;
        System.out.println("a += 5: " + a);
        a -= 5;
        System.out.println(" a -= 5: " + a);
        a *= 2;
        System.out.println("a *= 2 :" + a);
        a /=5;
        System.out.println("a/= 5: " + a);
        a %= 5;
        System.out.println("a %=5 :" + a);
    }
}
